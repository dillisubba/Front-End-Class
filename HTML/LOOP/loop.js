//while Loop
/* Question: Display number 1 to 5*/
var num1 = 1;
while (num1<=5)
{
    document.write(num1);
    num1++;
}
document.write ("<br>");

document.write ("<br>");

var age = 5;
while (age>=1)
{
document.write(age);
age--;
}

document.write ("<br>");
document.write ("<br>");




//do-while Loop
/* Question: Display number 1 to 5*/
var num1 = 1;
do{
    document.write(num1);
    num1++;
}
while(num1<=5);

document.write ("<br>");
document.write ("<br>");

var age = 5;
do{
    document.write(age);
    age--;
}
while(age>=1);

document.write ("<br>");
document.write ("<br>");


//for Loop
/* Question: Display number 1 to 5*/
for(var num1=1; num1<=5; num1++)
{
    document.write(num1);
}

document.write ("<br>");
document.write ("<br>");

for(var age=5; age<=1; age--)
{
    document.write(age);
}